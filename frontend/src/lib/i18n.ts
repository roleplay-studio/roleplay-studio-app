import { writable } from 'svelte/store';

/**
 * Detect the user's preferred language from the browser.
 *
 * Maps `navigator.language` (e.g. `'ru-RU'`, `'en-US'`, `'de'`) to one of
 * the supported `availableLangs()` ids. Falls back to `'en'` when the
 * browser language is not in the supported list or when `navigator` is
 * unavailable (SSR / tests).
 */
export function detectBrowserLang(): string {
  if (typeof navigator === 'undefined') return 'en';
  const raw = navigator.language || (navigator as any).userLanguage;
  if (!raw) return 'en';
  const short = raw.toLowerCase().split('-')[0];
  const supported = new Set(availableLangs().map((l) => l.id));
  return supported.has(short) ? short : 'en';
}

